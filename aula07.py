#Operadores aritiméticos

#Ordem de precedência de operadores
#1 ()
#2 **
#3 *, /, //, %
#4 +, -

num1 = int(input('Digite um valor: '))
num2 = int(input('Digite outro valor: '))

s = num1 + num2
m = num1 * num2
d = num1 / num2

print('A soma é {}, \nO produto é {} \nE a divisão é {}'.format(s, m, d))



